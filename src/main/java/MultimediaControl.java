/**
 * Interface for AudioPlayer and MoviePlayer that sets up methods.
 *
 * @author Christopher Dervan
 */
public interface MultimediaControl {

  /**
   * Method to play.
   */
  void play();

  /**
   * Method to stop.
   */
  void stop();

  /**
   * Method to previous.
   */
  void previous();

  /**
   * Method to next.
   */
  void next();

}
